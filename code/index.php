<h1>Introduction</h1>
<p2>The Diocese of Madurai and Ramnad has been functioning for the Glory of God in the historically famous cities of Tamilnadu. Our Diocese has its strong footing in the committed service rendered by missionaries. Church Missionary Society (CMS), The Society for the Propagation of the Gospel (SPG) and The American-Madura Mission were the three reputed organisations that stamped their service in our Diocese. Among the above said three Missionary Societies, the American-Madura Missions contribution is tremendous.</p2>
<p2>When the missionaries found the speedy and steady growth of these offspring of their handwork and labour, considered that the right time to hand over the mission to the Diocesan people. As a result, they founded Madurai Mission Sangam on 15th January 1934, and handed over the missionary projects and decided to back them up with their valuable guidance. This establishment had been functioning successfully from 1934 to 1947. On 27th September 1947, the Church of South India was formed and the Diocese of Madurai and Ramnad became a part of it, encompassing the two Districts Madurai and Ramnad. Now it holds 6 prominent districts Madurai, Ramnad, Dindigul, Sivagangai, Virudhunagar and Theni.</p2>

